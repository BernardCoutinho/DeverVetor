package Classes;

public class Contato{
    private int tel;
    private int condigo;
    private String nome;
   
   
   
    public int getTel() {
        return tel;
    }
    public void setTel(int tel) {
        this.tel = tel;
    }
    public int getCondigo() {
        return condigo;
    }
    public void setCondigo(int condigo) {
        this.condigo = condigo;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }

    

}